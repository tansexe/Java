public class switchExamples {
    public static void main(String[] args) {
        int number = 3;
        switch (number) {
            case 1:
                System.out.println("20");
                break;
            case 2:
                System.out.println("200");
                break;
            case 3:
                System.out.println("2000");
                break;

            default:
                break;
        }
    }
}
